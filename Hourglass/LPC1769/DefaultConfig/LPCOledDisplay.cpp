void LPCOledDisplay::StartTimeSettings(const std::uint32_t StartTime) {
    //#[ operation StartTimeSettings(uint32_t)
    uint32_t StartTime_Display = StartTime;
    int min = 0;
    int sec = 0;
    int ms = 0;
    
    while(StartTime_Display >= 60000){
    	StartTime_Display-=60000;
    	min++;
    }
    
    while(StartTime_Display >= 1000){
    	StartTime_Display-=1000;
    	sec++;
    }
    
    ms = StartTime_Display;
    
    char timeString[25];
    snprintf(timeString,sizeof(timeString), "%02lu:%02lu:%03lu\n", min, sec, ms);
    itsSSD1305.clrscr();
    itsSSD1305.print("  Startzeit\n\n");
    itsSSD1305.print(timeString);
    itsSSD1305.print("< klein gross >");
    itsSSD1305.print("\n\n\n");
    itsSSD1305.print("^ Zurueck");
    itsSSD1305.flush();
    
    //#]
}

