void LPCOledDisplay::emptyWatch_Signal(void) {
    //#[ operation emptyWatch_Signal()
    EmptyWatch_counter=50;
    while(EmptyWatch_counter!=0)
    {
    	if(EmptyWatch_counter%10==0)
    	{
    		itsSSD1305.bitmap(Pixels_Blink_Signal_One, 96, 64);
    		itsSSD1305.flush();
    	}
    	else if(EmptyWatch_counter%2!=0)
    	{
    		itsSSD1305.bitmap(Pixels_Blink_Signal_Zero, 96, 64);
    		itsSSD1305.flush();
    	}
    	EmptyWatch_counter--;
    }
    EmptyWatch_counter=50;
    //Signal Over
    itsSSD1305.bitmap(pixels, 96, 64);
    itsSSD1305.flush();
    
    
    
    //#]
}

void LPCOledDisplay::fallingSandDown(void) {
    //#[ operation fallingSandDown()
    bool notDetected = true;
    bool lastLineFound = false;
    int it = 0;
    int it1 = 3171;
    int it2 = 3219;
    
    while(notDetected && (it1 < 3220))
    {
    	if(pixels[it1]==1)
    	{
    		notDetected = false;
    		it = (it1) % 96;
    		while(it<6143)
    		{
    			if((pixels_state[it]) != 2  && (pixels_state[it] != 9)) 
    			{
    				pixels[it] = 0;
    			}
    			it += 96;
    		}
    	}
    	else if((pixels_state[it1] == 8))
    	{
    		return;
    	}
    	it1++;
    }
    
    while(!lastLineFound)
    {
    	if((pixels_state[it2] == 9) || (pixels[it2] == 1))
    	{
    		lastLineFound = true;
    		it = (it2) % 96;
    		while(it<6143)
    		{
    			if((pixels_state[it-1]) != 2  && (pixels_state[it-1] != 9)) 
    			{
    				pixels[it-1] = 1;
    			}
    			it += 96;
    		}
    	}
    	it2++;
    }
    
    itsSSD1305.bitmap(pixels, 96, 64);
    itsSSD1305.flush();
    
    
    
    //#]
}

