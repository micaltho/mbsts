void LPCOledDisplay::Hourglass(const std::uint32_t update_currentTime_ms, const std::uint32_t startTime_ms) {
    //#[ operation Hourglass(uint32_t,uint32_t)
    if(update_currentTime_ms == startTime_ms && SandIsUp == false)
    {	
    	//bernehmen des initialen Bildes
    	for(int i =0; i< 6144; i++)
    	{
    		pixels[i] = pixel_init[i];
    	}
    	//Zeitschritt fr das Durchlaufen der Sandmenge berechnen
    	fallingSandFactor = startTime_ms/numOfRowsWithSand;
    	//Abgelaufene Zeit auf Startzeit setzen
    	lastCurrentTime = startTime_ms;
    	fromRightToLeft = true;
    	//Glass mit Sand befllen
    	fillGlassWithSand();
    	SandIsDown = true;
    }
    
    else if((update_currentTime_ms <= (lastCurrentTime - fallingSandFactor)) || ((lastCurrentTime != 0) && update_currentTime_ms == 0))
    {
    	if(!fromRightToLeft)
    	{
    		GravitationFromLeftToRight();
    	}
    	fallingSandDown();
    	lastCurrentTime = update_currentTime_ms;
    	fromRightToLeft = true;	
    	SandIsDown = true;
    	
    }
    else if(update_currentTime_ms >= (lastCurrentTime + fallingSandFactor))
    {
    	if(fromRightToLeft)
    	{
    		GravitationFromRightToLeft();
    	}
    	fallingSandUp();
    	lastCurrentTime = update_currentTime_ms;
    	fromRightToLeft = false;
    	SandIsDown = true;
    	SandIsUp = true;
    }
    
    else if(update_currentTime_ms == 0 && lastCurrentTime == 0 && SandIsDown)
    {
    	emptyWatch_Signal();
    	itsSSD1305.bitmap(pixels, 96, 64);
    	itsSSD1305.flush();
    	SandIsDown = false;
    }
    else if(update_currentTime_ms == startTime_ms && SandIsUp == true)
    {
    	emptyWatch_Signal();
    	itsSSD1305.bitmap(pixels, 96, 64);
    	itsSSD1305.flush();
    	SandIsUp = false;
    }
    
    
    //#]
}

