SSD1305::SSD1305(Interface ifc, uint8_t port, uint8_t pin):MonoChromeDisplay(ifc,port,pin),
        mode(2,7,DigitalInOut::Direction::OUTPUT),
        onoff(2,1,DigitalInOut::Direction::OUTPUT) {}


SSD1305::~SSD1305() {
	if (isFramebufferDynamic)
	{
	    delete [] framebuffer;
	}
}


bool SSD1305::initialize(uint32_t w, uint32_t h, uint8_t fill, uint8_t* buffer) {
    height=h;
    width=w;

    /* --Already allocated? */
    if (isFramebufferDynamic)
    {
       delete [] framebuffer;
    }

    /* --Allocate. */
    if (buffer == nullptr)
    {
       uint32_t size = (height * width) >> 3; /* --divide by 8. */
       framebuffer = new uint8_t[size];
       isFramebufferDynamic = true;
    }
    else
    {
       framebuffer = buffer;
       isFramebufferDynamic = false;
    }
    /* --Clear. */
    clrscr(fill);

    /* --Initialize device. */
    off();
    init();
    /* --Delay. */
    for (uint32_t i = 0; i < 0xffff; i++);
    on();
    return true;
}



void SSD1305::init() const {
    /*
     * Recommended Initial code according to manufacturer
     */
    cmd(CMD_SET_COLUMN_ADDRESS_LSB(2));          //set low column address
    cmd(CMD_SET_COLUMN_ADDRESS_MSB(2));          //set high column address
    cmd(CMD_SET_DISPLAY_START_LINE(0));          //(display start set)
    cmd(CMD_DEACTIVATE_SCROLL);                  //(stop horzontal scroll)
    cmd(CMD_SET_CONTRAST_CONTROL_BANK0);         //(set contrast control register)
    cmd(0x32);                                   //(contrast value, 0...0xFF)
    cmd(CMD_SET_BRIGHTNESS);                     //(brightness for color banks)
    cmd(0x80);                                   //(value: display on, RESET value for Brightness)
    cmd(CMD_SET_SEGMENT_REMAP(1));               //(set segment re-map)
#if 1
    cmd(CMD_SET_NORMAL_DISPLAY);                 //(set normal/inverse display)
#else
    cmd(CMD_SET_INVERSE_DISPLAY);                //(set inverse display)
#endif
    cmd(CMD_SET_MULTIPLEX_RATIO);                //(set multiplex ratio)
    cmd(0x3F);
    cmd(CMD_SET_DISPLAY_OFFSET);                 //(set display offset)
    cmd(0x40);
    cmd(CMD_MASTER_CONFIGURATION);               //(set dc-dc on/off)
    cmd(0x8E);                                   // Recommended Value, external VCC supply
#if 1
    cmd(CMD_SET_COM_OUTPUT_DIR(1));              //(set com output scan direction)
#else
    cmd(CMD_SET_COM_OUTPUT_DIR(0));              //(set com output scan direction)
#endif
    cmd(CMD_SET_CLOCK_DIV_RATIO);                //(set display clock divide ratio/oscillator/frequency)
    cmd(0xf0);//
    cmd(CMD_SET_LOW_POWER_MODE);                 //(set area color mode on/off & low power display mode )
    cmd(0x05);//
    cmd(CMD_SET_PRE_CHARGE_PERIOD);              //(set pre-charge period)
    cmd(0xF1);
    cmd(CMD_SET_COM_PINS_HW_CONFIG);             //(set com pins hardware configuration)
    cmd(0x12);
    cmd(CMD_SET_VCOM_DESELECT_LEVEL);            //(set vcom deselect level)
    cmd(0x34);
    cmd(CMD_SET_LUT);                            //(set look up table for area color)
    cmd(0x3f);
    cmd(0x3f);
    cmd(0x3f);
    cmd(0x3f);
    cmd(CMD_SET_NORMAL_BRIGHTNESS_ON);           //(display on)
    cmd(CMD_SET_ENTIRE_DISPLAY_ON);              //(display on)
}


void SSD1305::on() const {
    onoff.set();
}

void SSD1305::off() const {
    onoff.clear();
}

bool SSD1305::cmd(uint8_t* buffer, uint32_t& len) const {
    /* --Switch to command mode. */
    commandMode();
    return write(buffer,len);
}

bool SSD1305::data(uint8_t* buffer, uint32_t& len) const {
    /* --Switch to data mode. */
    dataMode();
    return write(buffer,len);
}

bool SSD1305::cmd(uint8_t value) const {
    uint32_t len=1;
    return cmd(&value,len);
}

bool SSD1305::data(uint8_t value) const {
    uint32_t len=1;
    return data(&value,len);
}


void SSD1305::commandMode() const {
    mode.clear();
}

void SSD1305::dataMode() const {
    mode.set();
}


bool SSD1305::flush() const {

    for(uint8_t page=0;page<8;page++) {
        cmd(CMD_SET_PAGE_START_ADRESS(page));
        cmd(CMD_SET_COLUMN_ADDRESS_LSB(2));
        cmd(CMD_SET_COLUMN_ADDRESS_MSB(1));

        uint8_t* start=framebuffer + page*width;
        uint32_t len=width;
        data(start,len);
    }

    return true;
}





